// Helper function to get event by slug
export function getEventBySlug(slug) {
  return events.find((event) => event.slug === slug);
}

// Helper function to get all event slugs (for static generation)
export function getAllEventSlugs() {
  return events.map((event) => event.slug);
}

// Helper function to get events by status
export function getEventsByStatus(status) {
  return events.filter((event) => event.status === status);
}

// Helper function to get upcoming events
export function getUpcomingEvents() {
  return events.filter((event) => event.status === "upcoming");
}

// Helper function to get past events
export function getPastEvents() {
  return events.filter((event) => event.status === "past");
}
